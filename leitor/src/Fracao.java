
public class Fracao {
	// atributos
	
	private int numererador;
	private int denomenador;
	
	// Construtor
	// Construtor nao tem retorno
	
	public Fracao(int numerador, int denominador) {
		this.numererador = numerador;
		this.denomenador = denominador;
	}
	
	
	// Metodos
	public void apresentar() {
		System.out.println(numererador + "/" + denomenador);
	}

}
